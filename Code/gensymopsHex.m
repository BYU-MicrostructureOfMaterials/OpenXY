function [SymOps]=gensymopsHex
    for j=1:24
        switch j
            case 1 %E
                S=[1 0 0;...
                    0 1 0 ;...
                    0 0 1];
            case 2 %C2
                S=[-1 0 0;...
                    0 -1 0;...
                    0 0 1];
            case 3 %C3+
                S=[-1/2 sqrt(3)/2 0;...
                    -sqrt(3)/2 -1/2 0;...
                    0 0 1];               
            case 4 %C3-
                S=[...
                        -1/2 -sqrt(3)/2 0;...
                        sqrt(3)/2 -1/2 0;...
                        0 0 1];
            case 5 %C6+
                S=[...
                        1/2 sqrt(3)/2 0;...
                        -sqrt(3)/2 1/2 0;...
                        0 0 1];
            case 6 %C6-
                S=[...
                        1/2 -sqrt(3)/2 0;...
                        sqrt(3)/2 1/2 0;...
                        0 0 1];
            case 7 %C21'
                S=[...
                        -1 0 0;...
                        0 1 0;...
                        0 0 -1];
                
            case 8
                S=[...
                        1/2 -sqrt(3)/2 0;...
                        -sqrt(3)/2 -1/2 0;...
                        0 0 -1];
                
            case 9
                S=[...
                        -1/2 sqrt(3)/2 0;...
                        sqrt(3)/2 1/2 0;...
                        0 0 -1];
                
            case 10 %C21''
                S=[...
                        1 0 0;...
                        0 -1 0 ;...
                        0 0 -1];
                
            case 11
                S=[...
                        -1/2 -sqrt(3)/2 0;...
                        -sqrt(3)/2 1/2 0 ;...
                        0 0 -1];
                
            case 12
                S=[...
                        1/2 sqrt(3)/2 0;...
                        sqrt(3)/2 -1/2 0 ;...
                        0 0 -1];
        end    
        SymOps(j,:,:)=S;
    end
        